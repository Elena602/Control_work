Решение задачи
Задаем количество строк и стобцов с клавиатуры. Метод FillArray задает рандомные числа в интервале от -1500 до 1500, 
Метод PrintArray далее при вызове позволит распечатать массив, заданный в FillArray. Далее необходим метод FillArray2 для того, чтобы 
отсечь 4-хзначные числа и вывести 3-х и менее -значные. Вызываем методы FillArray и PrintArray для того, чтобы заполнить и вывести изначальный массив, 
Далее печатаем "Новая матрица: " и вызываем метод FillArray, сразу печатая его.